/**
 * ABC446 B - Greedy Draft
 */

import { describe, expect, test } from '@jest/globals';
import { solve } from '../../src/problems/b/b-8.js';

describe('ABC446 B - Greedy Draft', () => {
  // --------------------------------------------------------
  // 公式サンプル
  // --------------------------------------------------------
  describe('公式サンプルテスト', () => {
    test('サンプル1: 4人5本', () => {
      const input = `4 5
3
3 1 2
3
3 2 1
2
2 3
4
2 5 3 1`;
      expect(solve(input)).toBe('3\n2\n0\n5');
    });

    test('サンプル2: 6人5本', () => {
      const input = `6 5
1
3
2
3 5
5
5 3 1 4 2
5
5 1 3 4 2
5
3 4 1 5 2
5
5 1 3 2 4`;
      expect(solve(input)).toBe('3\n5\n1\n4\n2\n0');
    });
  });

  // --------------------------------------------------------
  // エッジケース
  // --------------------------------------------------------
  describe('エッジケース', () => {
    test('客1人・缶1本: 希望リストに該当あり', () => {
      const input = `1 1
1
1`;
      expect(solve(input)).toBe('1');
    });

    test('客1人・缶1本: 希望リストが空', () => {
      const input = `1 1
0
`;
      expect(solve(input)).toBe('0');
    });

    test('全客が同じ缶だけ希望: 客1だけ取得、残りは水', () => {
      const input = `3 1
1
1
1
1
1
1`;
      expect(solve(input)).toBe('1\n0\n0');
    });

    test('全員が異なる缶を希望: 全員取得', () => {
      const input = `3 3
1
1
1
2
1
3`;
      expect(solve(input)).toBe('1\n2\n3');
    });

    test('後の客が先頭の缶を先取りされても別の缶を取れる', () => {
      // 客1: [1,2] → 1取得 / 客2: [1,2] → 1はない → 2取得
      const input = `2 2
2
1 2
2
1 2`;
      expect(solve(input)).toBe('1\n2');
    });

    test('缶が余っても希望リストにない場合は水', () => {
      // M=3 だが客2の希望は缶1のみ(すでに取られている)
      const input = `2 3
1
1
1
1`;
      expect(solve(input)).toBe('1\n0');
    });
  });

  // --------------------------------------------------------
  // 順序・優先度のテスト
  // --------------------------------------------------------
  describe('順序・優先度のテスト', () => {
    test('番号が大きい缶がリスト先頭にある場合、番号順ではなくリスト順で選ぶ', () => {
      // 客1の希望: [5, 1] → 先頭の5を選ぶ
      const input = `1 5
2
5 1`;
      expect(solve(input)).toBe('5');
    });

    test('競合が連鎖するケース: N人全員が同じリスト', () => {
      // 客1→1, 客2→2, 客3→3, 客4→水
      const input = `4 3
3
1 2 3
3
1 2 3
3
1 2 3
3
1 2 3`;
      expect(solve(input)).toBe('1\n2\n3\n0');
    });

    test('希望リストの先頭ではなく途中の缶を選ぶケース', () => {
      // 客1: [1] → 1取得 / 客2: [1,2] → 1はない → 2取得
      const input = `2 2
1
1
2
1 2`;
      expect(solve(input)).toBe('1\n2');
    });
  });
});
